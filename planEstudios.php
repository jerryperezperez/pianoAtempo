 <h1 >Tenemos un esquema de aprendizaje construido para cualquier estudiante</h1>

 <p class="textoPrincipal">En Piano A Tempo cubrimos todos los niveles de enseñanza. Contamos con una estructura de estudio que permitirá a cualquier estudiante de cualquier nivel aumentar sus habilidades con el instrumento.</p>
 <p class="textoPrincipal">A continuación se le muestra algunos temas que se abarcan durante la enseñanza</p>

 <h2 class="subtituloNivel">PRINCIPIANTE</h2>
 <ul class="listaPlan">
   <li>Fundamentos del teclado, Patrones visuales, Posición correcta, Pulso básico.</li>
   <li>Técnica de digitación, Formación de la escala mayor, Intervalos.</li>
   <li>Armonía, Técnica de Do movible: digitación en C. Técnica de Do movible:
digitación en B.</li>
   <li>Armonía: Sistema Do movible, Ejercicios de digitación de escala mayor,
Melodías rítmicas.</li>
   <li>Armonía: introducción a las escalas menores, Progresiones armónicas.</li>
   <li>Triadas aumentadas, Fundamentos de las escalas menores, Localización de octavas
en notas negras.</li>
   <li>Escalas menores en E, Armonización de escalas menores en E, Acordes menores
con séptima parte 1, Progresión rítmica I-IV-V parte 1, Digitación inversa en Ab.</li>
   <li>Escalas menores en F#, Armonización escalas menores en F#, Arpegios con
acordes mayores con 7as, Introducción a la progresión II V I.</li>
   <li>Escalas menores en Eb, Armonización de escalas menores en Eb, Inversiones de
acordes menores con 7ª, Acordes de 6ª mayor, Digitación inversa en D.</li>
   <li>Arpegios dominantes con 7ª, Armonización de escalas mayores a 6 voces.</li>
 </ul>

 <h2 class="subtituloNivel">INTERMEDIO</h2>
<ul class="listaPlan">
  <li>Progresión II V I parte 2, Ritmos latinos parte 1, Introducción a la progresión I VI
II V, Escala mayor inversa en E.</li>
  <li>Escalas menores en Bb, Armonización de escalas menores en Bb, Acordes menor
con 6ª, Progresión II V I (parte 3), Aplicación del sistema Do Movible.</li>
  <li>Escalas menores en Ab. Armonización de escalas menores en Ab. Ritmos latinos
(parte 2). Acordes menores con 6ª (parte 2). Acordes suspendidos con 4ª (parte1).</li>
  <li>Escalas mayores en 6ª. Figuras armónicas 3 sobre 2 (sus 9). Acompañamientos
con 6ª. Ritmos latinos (parte 3).</li>
  <li>Figuras armónicas 4/1. Arpegios min9. Arpegios con 6as (continuación). Escala
cromática.</li>
  <li>Escala pentatónica en D. Escala de blues en D. Acordes mayores con 13va.</li>
  <li>Escala pentatónica en E. Escala de blues en E. Tónicas implícitas para acordes
mayores (parte 1). Ritmo de blues mano izquierda (parte 1).</li>
  <li>Escala pentatónica en F. Escala de blues en F. Ritmo de blues parte 2: mano
derecha. Tónicas implícitas para acordes menores.</li>
  <li>Escala pentatónica en G. Escala de blues en G. Ritmo de blues tercera parte.
Patrones horizontales (continuación).</li>
  <li>Escala pentatónica en A. Escala de Blues en A. Patrones horizontales (continuación).
Ritmo de blues. Progresión II – V – I menor.
</li>
</ul>
 <h2 class="subtituloNivel">AVANZADO</h2>
<ul class="listaPlan">
<li>Escala pentatónica en B. Escala de blues en B. Progresiones cromáticas parte 1.
Patrones horizontales.</li>
  <li>Escala pentatónica en C#. Escala de blues en C#. Patrones horizontales.
Progresiones cromáticas.</li>
  <li>Escala pentatónica en Eb. Escala de blues en Eb. Progresiones cromáticas.
Patrones horizontales.</li>
  <li>Escala pentatónica en F#. Escala de blues en F#. Progresiones cromáticas (continuación).
Progresiones modales. Introducción a los montunos.</li>
  <li>Escala pentatónica en Ab. Escala de blues en Ab. Progresiones armónicas.
Progresiones modales. Montunos (parte 2).</li>
  <li>Escala pentatónica en Bb#. Escala de blues en Bb. Montunos clave 3-2.
Progresiones modales.</li>
</ul>

